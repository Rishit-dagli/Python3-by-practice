# int
var_int = 3
print("int = ", var_int)

# float
var_float = 3.0
print("float = ", var_float)

# string
var_string = "3.0x"
print("float = ", var_string)

# list
var_list = [10, 3.0, "hello"]
print("list = ", var_list)
print("var_list[2] is ", var_list[2])

# dictionary
var_dictionary = {"first_name": "konghiran", "last_name": "wongchairungruang"}
print("dictionary = ", var_dictionary)
print("my name is ", var_dictionary["first_name"])

# boolean
var_bool = True
print("var_bool = ", var_bool)

# Tuple
var_tuple = (10, "hello", 200.3)
print("var_tuple = ", var_tuple)
print("var_tuple[2] is ", var_tuple[2])

